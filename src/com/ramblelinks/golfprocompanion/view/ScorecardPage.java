package com.ramblelinks.golfprocompanion.view;

public class ScorecardPage {
	
	
	private String zipcode;		
	private String golfcourse;
	private String playdate;
	private String teetype;	
	private String city;
	private String city_id;
	
	
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
		
	public String getGolfcourse() {
		return golfcourse;
	}
	public void setGolfcourse(String golfcourse) {
		this.golfcourse = golfcourse;
	}
	
	public String getPlaydate() {
		return playdate;
	}
	public void setPlaydate(String playdate) {
		this.playdate = playdate;
	}
		
	public String getTeetype() {
		return teetype;
	}
	public void setTeetype(String teetype) {
		this.teetype = teetype;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCity() {
		return city;
	}
	public void setCity_id(String city_id) {
		this.city_id = city_id;
	}
	public String getCity_id() {
		return city_id;
	}	

	
	

}
